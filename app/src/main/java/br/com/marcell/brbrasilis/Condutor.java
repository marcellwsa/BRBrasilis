public class Condutor {

    private int codigo;
    private String placaCondutor;
    private String v;
    private String nome;
    private String CPF;
    private String naturalidade;
    private String estadoCivil;
    private String telefone;
    private String escolaridade;
    private String endereco;
    private String ocupacao;
    private String cinto;
    private String etilometro;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getPlacaCondutor() {
        return placaCondutor;
    }

    public void setPlacaCondutor(String placaCondutor) {
        this.placaCondutor = placaCondutor;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public String getNaturalidade() {
        return naturalidade;
    }

    public void setNaturalidade(String naturalidade) {
        this.naturalidade = naturalidade;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEscolaridade() {
        return escolaridade;
    }

    public void setEscolaridade(String escolaridade) {
        this.escolaridade = escolaridade;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getOcupacao() {
        return ocupacao;
    }

    public void setOcupacao(String ocupacao) {
        this.ocupacao = ocupacao;
    }

    public String getCinto() {
        return cinto;
    }

    public void setCinto(String cinto) {
        this.cinto = cinto;
    }

    public String getEtilometro() {
        return etilometro;
    }

    public void setEtilometro(String etilometro) {
        this.etilometro = etilometro;
    }
}
